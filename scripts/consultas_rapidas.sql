    metrica,
    COUNT(*) as "Registros",
    ROUND(SUM(valor_gwh)::numeric, 2) as "Total GWh",
    ROUND(AVG(valor_gwh)::numeric, 2) as "Promedio GWh"
FROM metrics
WHERE fecha >= CURRENT_DATE - INTERVAL '7 days'
GROUP BY metrica
ORDER BY "Total GWh" DESC;

